/// 六曜 (Rokuyō) calculator
///
/// Uses pre-computed lookup table for 2025-2028 (verified against arachne.jp)
/// Falls back to lunar calendar formula for dates outside the table range
class RokuyoCalculator {
  final LunarCalendar _lunarCalendar;

  RokuyoCalculator({LunarCalendar? lunarCalendar})
      : _lunarCalendar = lunarCalendar ?? LunarCalendar.instance;

  /// Calculate the Rokuyō for a given Gregorian date
  Rokuyo calculate(DateTime date) {
    final key =
        '${date.year}-${date.month.toString().padLeft(2, '0')}-${date.day.toString().padLeft(2, '0')}';
    final tableValue = _rokuyoTable[key];
    if (tableValue != null) {
      return _fromIndex(tableValue);
    }

    // Fallback: lunar calendar formula for dates outside table range
    final lunar = _lunarCalendar.toLunar(date);
    if (lunar == null) return Rokuyo.unknown;
    return _fromIndex((lunar.absoluteMonth + lunar.day) % 6);
  }

  static Rokuyo _fromIndex(int index) {
    switch (index) {
      case 0: return Rokuyo.taian;
      case 1: return Rokuyo.shakku;
      case 2: return Rokuyo.senshou;
      case 3: return Rokuyo.tomobiki;
      case 4: return Rokuyo.senbu;
      case 5: return Rokuyo.butsumetsu;
      default: return Rokuyo.unknown;
    }
  }

